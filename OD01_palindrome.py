# Описание алгоритма для определения, является ли строка палиндромом

# 1. Преобразование строки:
# Приведите строку к единому регистру, например, ко всем строчным буквам.
# Это поможет избежать ошибок при сравнении символов разного регистра.
# Удалите из строки все неалфавитные символы и пробелы, чтобы учитывать только буквы и цифры.
# Это можно сделать, проходя по строке и сохраняя только символы, которые являются буквами или цифрами.

# 2. Инициализация указателей:
# Установите два указателя: один в начало строки (на первый символ), а другой в конец строки (на последний символ).

# 3. Проверка символов:
# Пока левый указатель меньше или равен правому указателю:
# Сравните символы, на которые указывают левый и правый указатели.
# Если символы не совпадают, строка не является палиндромом.
# Вы можете сразу завершить алгоритм, возвращая False.
# Если символы совпадают, сдвиньте левый указатель на одну позицию вправо, а правый указатель — на одну позицию влево.

# 4. Заключение:
# Если все пары символов совпали (в случае, если цикл завершился без обнаружения несовпадений),
# строка является палиндромом. Верните True.

# Функция на основе описанного алгоритма
def is_palindrome(s: str) -> bool:
    # Преобразование строки: приведение к нижнему регистру и удаление неалфавитных символов
    cleaned_s = ''.join(char for char in s.lower() if (char.isalpha() or char.isdigit()))

    # Инициализация указателей
    left, right = 0, len(cleaned_s) - 1

    # Проверка символов
    while left <= right:
        if cleaned_s[left] != cleaned_s[right]:
            return False  # Строка не является палиндромом
        left += 1
        right -= 1

    return True  # Строка является палиндромом

# Пример использования
print(is_palindrome("Шалаш")) # True
print(is_palindrome("Алгоритм")) # False
print(is_palindrome("А луна, как бы рыбка, канула")) # True
print(is_palindrome("Палиндром — это число, буквосочетание, слово или текст, одинаково читающееся в обоих направлениях")) # False
print(is_palindrome("Level")) # True
print(is_palindrome("Palindrome")) # False
print(is_palindrome("A man, a plan, a canal, Panama"))  # True
print(is_palindrome("Hello, World!"))  # False
print(is_palindrome("123454321")) # True
print(is_palindrome("2357111317")) # False
print(is_palindrome("123топот321")) # True
